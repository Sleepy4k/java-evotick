package com.evotick.model;

import java.io.Serializable;
import java.util.UUID;

/**
 *
 * @author Pandu
 */
public class EventStatus implements Serializable {

  private UUID id;

  private String name;

  public EventStatus() {
  }

  public UUID getId() {
    return id;
  }

  public void setId(UUID id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

}
